export function getNameById(nameId: string): AllahName | undefined {
  return allNames.find(name => name.nameId === nameId);
}

export function getActiveNames(): AllahName[] {
  return allNames.filter(name => name.hasContent && name.approved);
}

export function getApprovedNames(): AllahName[] {
  return allNames.filter(name => name.approved);
}

export function getUnapprovedNames(): AllahName[] {
  return allNames.filter(name => !name.approved);
}

export function getDisplayNumber(nameId: string): number {
  const approved = getApprovedNames();
  const idx = approved.findIndex(n => n.nameId === nameId);
  return idx >= 0 ? idx + 1 : 0;
}

export function getComingSoonNames(): AllahName[] {
  return allNames.filter(name => !name.hasContent);
}
